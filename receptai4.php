<h2>Krevetes sonines patale</h2><br>
<img src="https://www.receptai.lt/uploads/modules/recipes/fullsize/10197.jpg" alt = "Krevetes sonines patale" width = "200" height = "170">
<p><b>Paruosimo laikas:</b> apie 20 min.<br>
<b>Porciju skaicius:</b> 5</p><hr>

<ol><b>Ingredientai:</b>
  <li><em><text style="color:red;">Krevetes</text></em>, 20 vienetu</li>
  <li><em><text style="color:red;">Saltai rukyte sonine</text></em>, 10 riekiu</li>
  <li><em><text style="color:red;">Kotanyi zuvies kepsniu prieskoniai</text></em>, 1 ziupsnelis</li>
</ol>

<ul><b>Gaminimo eiga:</b>
  <li>Šoninės juosteles perpjauti perpus, įtrinti prieskoniais.</li>
  <li>Kiekviena juostelės puse apvynioti po krevetę.</li>
  <li>Kepti grilyje iš abiejų pusių, kol šoninė pasidarys traški.</li>
  <li>Skanauti su klevų sirupu.</li>
</ul>

