<h2>Keptos krevetes</h2><br>
<img src ="https://www.receptai.lt/uploads/modules/recipes/thumb380x250/10164.jpg" alt = "Keptos krevetes" width = "200" height = "180">
<p><b>Paruosimo laikas:</b> apie 10min.<br>
<b>Porciju skaicius:</b> 2</p><hr>

<ol><b>Ingredientai:</b>
  <li><em><text style="color:red;">Krevetes</text></em>, 300 gramu</li>
  <li><em><text style="color:red;">Baltas vynas</text></em>, 100 mililitru</li>
  <li><em><text style="color:red";>Pipirai</text></em>, 1 ziupsnelis</li>
</ol>

<ul><b>Gaminimo eiga:</b>
  <li>Krevetes 5 minutes pamirkyti vyne. Nuvarvinti.</li>
  <li>Sumauti ant iešmelių, pagardinti pipirais.</li>
  <li>Kepti ant įkaitusių anglių, po 3 minutes kiekvieną pusę.</li>
</ul>






